a = 10
b = 23
print(a + b) # addition
print(a - b) # Subtraction
print(a * b) # Multiplication
print(a / b) # Division
print(a % b) # Remainder Operator(Modulus)
print(a // b) # Integer Division(Gives answer in Integer)



# Comparison Operators
# == (Equal)
print(a == b) # (False)

# != (Not Equal)
print(a != b) # (True)

# > (Greater than)
print(a > b) # (False)

# < (Less than)
print(a < b) # (True)

# >= (Greater than or equal to)
print(a >= b) # (False)

# <= (Less than or equal to)
print(a <= b) # (True)


# Logical Operators
a  = 2
b = 24


print(a > 5 and b > 20 and a > 1)
#     False-----True-------True

print(a > 5 or b > 30 or a > 1)
#     False----False------True

print(not a > 3)

print(a > 1 and b != 30 or a > 0)

